public class HomeWork1 {
    public static void main(String[] args) {

        // Создать переменные всех примитивных
        // типов и вывести их значения на экран

        boolean MyBoolean = false; // (false-true)
        System.out.println(MyBoolean);

        byte MyByte = 126; // 8 bits
        System.out.println(MyByte);

        short MyShort = 29765; // 16 bits
        System.out.println(MyShort);

        int MyInt = 1443245; // 32 bits
        System.out.println(MyInt);

        long MyLong = 42565422; // 64 bits
        System.out.println(MyLong);

        float MyFloat = 43.42f; // 32 bits
        System.out.println(MyFloat);

        double MyDouble = 43.466; // 64 bits
        System.out.println(MyDouble);

        char MyChar = 'g'; // 16 bits
        System.out.println(MyChar);


    }
}